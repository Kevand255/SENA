//validar si una variable es tipo
// numérica
let x = 67;
if (typeof x === "number"){
    console.log("La variable es numérica");
}else{
    console.log("La variable no es numérica");
}

//verificar si una variable es string
let y = "ADSO 682";
if (typeof y === "string"){
    console.log("La variable es cadena");
}else{
    console.log("La variable no es cadena");
}

//verificar si una variable es booleana
let z = true;
if (typeof z === "boolean"){
    console.log("La variable es booleana");
}else{
    console.log("La variable no es booleana");
}